public class Demo07 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-32));//绝对值
        System.out.println(Math.ceil(3.14));//向上取整
        System.out.println(Math.floor(3.56));//向下取整
        System.out.println(Math.round(3.49));//四舍五入
    }
}
